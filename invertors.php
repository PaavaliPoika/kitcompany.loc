1.	Мережевий (синхронний) інвертор: дозволяє забезпечити себе електроенергією, та продавати надлишки до магістральної мережи. Основною превагою такого рішення є те, що правильно спроектована та встановлена система може працювати роками без втручання та обслуговування (не потребує дозаправок, замін, контролю та ін.) та приносить прибуток від продажу надлишків електроенергії. 
